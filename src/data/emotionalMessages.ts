export function getUniqueEmotionalMessage(): string {
  const indexStr = localStorage.getItem('emotional_msg_index') || '0';
  let index = parseInt(indexStr, 10);
  
  if (isNaN(index) || index >= EMOTIONAL_HISTORY_MESSAGES.length) {
    // Shuffle the array indices if we reached the end or it's invalid
    index = 0;
  }
  
  const message = EMOTIONAL_HISTORY_MESSAGES[index];
  localStorage.setItem('emotional_msg_index', (index + 1).toString());
  
  return message;
}
